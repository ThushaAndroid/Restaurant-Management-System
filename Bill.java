                epricebox.setText(endprice);
                SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
                date=sdf.format(datebox.getDate());
                fprice=eprice;
                
            String sql="INSERT INTO bill(bid,bfood,btype,bprice,bqty,beprice,bdate,fprice) VALUES ('"+id+"','"+food+"','"+type+"','"+price+"','"+qty+"','"+eprice+"','"+date+"','"+fprice+"')";
            pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Inserted");
            

