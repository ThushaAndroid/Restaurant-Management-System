                food=Table.getValueAt(r, 0).toString();
                type=Table.getValueAt(r, 1).toString();
                price=Table.getValueAt(r, 2).toString();
               
                switch(price){
                    case "500.0":
                        p=500;
                        break;
                        case "700.0":
                        p=700;
                        break;
                        case "550.0":
                        p=550;
                        break;
                        case "750.0":
                        p=750;
                        break;
                        case "800.0":
                        p=800;
                        break;
                        case "1250.0":
                        p=1250;
                        break;
                        case "950.0":
                        p=950;
                        break;
                        case "1750.0":
                        p=1750;
                        break;
                        case "850.0":
                        p=850;
                        break;
                        case "1600.0":
                        p=1600;
                        break;
                        
                }
                
                qty=Integer.parseInt(qtybox.getSelectedItem().toString());
